/*
    How to print something on console :
        System -> It is a class.
        out -> It is a object.
        println()/print()/printf()/format() -> these are function.

        System.out.println("Hello, World!");
         * this will print the output given to it and also add a newline.

        System.out.print("Hello, World!");
         * this will print the output given to it.

        System.out.printf("Hello, World!");
         * this will print the output given to it.
         * if you familiar with c it also the same.

        System.out.format("Hello, World!");
         * this will print the output given to it.
         * if you familiar with c it also the same.
 */



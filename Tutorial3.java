/*
    Datatype :
        * int
          It stores integer values.

        * float
            It stores decimal values.
            By default, every decimal value are double in java.

        * double
            It stores big decimal values.

        * char
            It stores alphabets.

        * boolean
            It stores true/false

        * String
            It stores string/sentence/word/paragraph

        * byte
            It stores integer -128 to 127

        * short
            It stores twice to the byte

        * long
            It stores big integer values.

        * final -> it means that this value can't be changed.
                   it is constant.

 */

public class Tutorial3 {
    public static void main(String[] args) {
        int a = 5;
        boolean b = true; // true/false
        String str = "Vansh Tomar";
        char c = 'A';
        double d = 23.44;
        final float e = 33.33f;
    }
}
